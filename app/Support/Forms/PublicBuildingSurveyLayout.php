    public static function section(string $name): array
    {
        if ($name !== 'Unit_Information') {
            return parent::section($name);
        }

        $sections = static::sections();
        $repeatSection = $sections['Unit_Information'] ?? [
            'type' => 'repeat',
            'name' => 'Unit_Information',
            'label' => 'Unit Information',
            'hint' => null,
            'fields' => [],
        ];

        $repeatSection['fields'] = collect(self::UNIT_INFORMATION_SECTION_NAMES)
            ->flatMap(fn (string $sectionName): array => $sections[$sectionName]['fields'] ?? [])
            ->values()
            ->all();

        return $repeatSection;
    }

    public static function repeatSections(string $name): array
    {
        if ($name !== 'Unit_Information') {
            return parent::repeatSections($name);
        }

        $sections = static::sections();

        return collect(self::UNIT_INFORMATION_SECTION_NAMES)
            ->mapWithKeys(fn (string $sectionName): array => [$sectionName => $sections[$sectionName] ?? [
                'type' => 'group',
                'name' => $sectionName,
                'label' => $sectionName,
                'hint' => null,
                'fields' => [],
            ]])
            ->all();
    }

